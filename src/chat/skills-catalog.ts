/**
 * 关键词匹配搜索，返回最相关的前3个技能
 */
export function searchCatalog(description: string): CatalogSkill[] {
  const desc = description.toLowerCase();
  const scored = SKILLS_CATALOG.map((skill) => {
    let score = 0;
    // 关键词匹配
    for (const kw of skill.keywords) {
      if (desc.includes(kw.toLowerCase())) {
        score += 2;
      }
    }
    // 名称/描述部分匹配
    if (desc.includes(skill.name.toLowerCase())) score += 3;
    if (skill.description.toLowerCase().split(/[，,、\s]+/).some(w => w.length >= 2 && desc.includes(w))) {
      score += 1;
    }
    // 分类匹配
    const categoryKeywords: Record<string, string[]> = {
      business: ["商业", "战略", "分析", "市场", "business"],
      content: ["内容", "写作", "文案", "报告", "撰写"],
      finance: ["财务", "金融", "税务", "融资", "财税"],
      legal: ["法务", "法律", "合同", "合规"],
      product: ["产品", "技术", "开发", "代码"],
      marketing: ["营销", "增长", "推广", "运营", "流量"],
      efficiency: ["效率", "计划", "管理", "复盘", "学习"],
    };
    for (const kw of (categoryKeywords[skill.category] || [])) {
      if (desc.includes(kw)) score += 1;
    }
    return { skill, score };
  });

  return scored
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3)
    .map(({ skill }) => skill);
}
